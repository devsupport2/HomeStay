package com.ui.model;

public class Status {
	int statusId;
	String statusName;
	String active;
	int createdBy;
	String createdDate;
	String ipAddress;

	public int getStatusId() {
		return statusId;
	}

	public String getStatusName() {
		return statusName;
	}

	public String getActive() {
		return active;
	}

	public int getCreatedBy() {
		return createdBy;
	}

	public String getCreatedDate() {
		return createdDate;
	}

	public String getIpAddress() {
		return ipAddress;
	}
}
